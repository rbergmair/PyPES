# -*-  coding: ascii -*-  # # # # # # # # # # # # # # # # # # # # # # # # # # #

import sys;

from pypes.infer._preprocessing.rte_sanitizer import sanitize_rte;



# # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # #

def main( argv=None ):
  
  with open( argv[1], mode="rb" ) as f:
    with open( argv[2], mode="wt", encoding="utf-8" ) as g:
      sanitize_rte( f, g );



# # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # #

if __name__ == '__main__':
  sys.exit( main( sys.argv ) );



