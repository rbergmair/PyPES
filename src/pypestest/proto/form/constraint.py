__package__ = "pypestest.proto.form";
__all__ = [ "TestConstraint", "suite", "main" ];

import sys;
import unittest;

from pypes.utils.unittest_ import TestCase;
from pypes.utils.mc import object_;

from pypes.proto import *;


# # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # #

class TestConstraint( TestCase, metaclass=object_ ):
  
  def test_instantiate_constraint( self ):
    
    inst1 = Constraint(
                harg = Handle( hid=1 ),
                larg = Handle( hid=2 )
              );
              
    self.assertFalse( isinstance( inst1, Constraint ) );
    
    form = ProtoForm();
    inst2 = inst1( pf=form );
    self.assertTrue( isinstance( inst2, Constraint ) );
    

# # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # #

def suite():

  suite = unittest.TestSuite();

  suite.addTests( unittest.TestLoader().loadTestsFromTestCase(
      TestConstraint
    ) );

  return suite;


# # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # #

def main( argv=None ):

  unittest.TextTestRunner( verbosity=2 ).run( suite() );

if __name__ == '__main__':
  sys.exit( main( sys.argv ) );


